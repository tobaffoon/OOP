package ru.nsu.amazyar.entities;

/**
 * Object on 2d grid.
 */
public interface Entity {

    /**
     * X coordinate getter.
     */
    int getX();

    /**
     * X coordinate setter.
     */
    void setX(int x);

    /**
     * Y coordinate getter.
     */
    int getY();

    /**
     * Y coordinate setter.
     */
    void setY(int y);
}
